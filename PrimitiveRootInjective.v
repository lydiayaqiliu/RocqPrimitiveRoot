Lemma order_divides :
  forall g p r : Z,
  primitive_root g p ->
  order_eq_pminus1 g p ->
  r > 0 ->
  g ^ r mod p = 1 ->
  ((p - 1) | r)%Z.
Proof.
  intros g p r Hprim Hord Hr Hgr.
  unfold order_eq_pminus1 in Hord.
  destruct Hord as [Hfull Hmin].

  (* Write r = (p-1)q + s by Euclidean division *)
  destruct (Z.div_eucl r (p - 1)) as [q s] eqn:Hdiv.

  assert (Hpbound : p - 1 > 0).
  { unfold primitive_root in Hprim.
    destruct Hprim as [Hprime _].
    apply Z.lt_gt.
    apply prime_ge_2 in Hprime.
    lia. }

  assert (Hlt : 0 < p - 1) by lia.
  assert (Hne : p - 1 <> 0) by lia.

  assert (Heucl := Z.div_eucl_eq r (p - 1)).
  rewrite Hdiv in Heucl.
  specialize (Heucl Hne).
  (* Heucl : r = (p - 1) * q + s *)

  assert (Hbound : 0 <= r mod (p - 1) < p - 1).
  { apply Z.mod_pos_bound. exact Hlt. }

  assert (Hs : s = r mod (p - 1)).
  { rewrite Heucl. rewrite Z.add_comm. rewrite Z.mul_comm. 
  rewrite Z.mod_add; try lia.
  apply Euclidean_division_mod with (r := r) (q := q).
  - assumption.
  - unfold primitive_root in Hprim. destruct Hprim. apply prime_ge_2 in H.
  lia.
  - assumption. 
  }

  assert (Hsbound : 0 <= s < p - 1).
  { rewrite Hs. exact Hbound. }

  assert (Hqnonneg : 0 <= q).
  { destruct Hsbound as [Hslo _].
  apply Euclidean_division_pos with (r := r) (p := p) (s := s); try (assumption).
  unfold primitive_root in Hprim. destruct Hprim. apply prime_ge_2 in H.
  lia.

  
  }

  assert (Hpow : (g ^ (p - 1)) ^ q mod p = 1).
{ rewrite <- Z.mod_pow_l.
  rewrite Hfull.
  rewrite Z.pow_1_l; try lia.
  apply Z.mod_1_l.
  unfold primitive_root in Hprim.
  destruct Hprim as [Hprime _].
  apply prime_ge_2 in Hprime. lia. }


  assert (Hrsplit : g ^ r = (g ^ (p - 1)) ^ q * g ^ s).
    { rewrite Heucl.
      rewrite Z.pow_add_r; try lia.
      rewrite Z.pow_mul_r; try lia.
      apply Z.mul_nonneg_nonneg; lia.
    }
  (* Show g^s ≡ 1 (mod p) *)
  assert (Hgs : g ^ s mod p = 1).
  {
    rewrite Hrsplit in Hgr.
    rewrite Z.mul_mod in Hgr; try lia.
    rewrite Hpow in Hgr.
    rewrite Z.mul_1_l in Hgr.

    rewrite Z.mod_mod in Hgr.
    - assumption.
    - unfold primitive_root in Hprim.
      destruct Hprim as [Hprime _].
      apply prime_ge_2 in Hprime. lia.
    }

  (* Since 0 <= s < p-1 and g^s ≡ 1, minimality forces s = 0 *)
  assert (Hs0 : s = 0).
  { destruct (Z.eq_dec s 0) as [Hs0 | Hs0].
    - exact Hs0.
    - exfalso.
      destruct Hsbound as [Hslo Hshi].
      assert (Hspos : 0 < s) by lia.
      exact (Hmin s Hspos Hshi Hgs).
  }

  exists q.
  rewrite Heucl.
  lia.
Qed.
      

