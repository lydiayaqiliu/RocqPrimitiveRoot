Lemma pow_split :
  forall g m k : Z,
  0 <= m /\ 0 <= k ->
  m >= k ->
  g ^ m = g ^ (m - k) * g ^ k.
Proof. intros.  destruct H. 
    replace m with (m - k + k) by lia. rewrite Z.pow_add_r; try (lia).
    replace (m - k + k - k) with (m - k).
    reflexivity. lia.
Qed.
    



