(*Now we prove that primitive root construction is indeed injective
 (i.e., {p^0,...,p^{p-2} are all distinct.})*)
Definition coprime (a b: Z) :Prop := Z.gcd a b = 1.


Definition primitive_root (g p : Z) : Prop :=
    prime p /\
    coprime g p /\
    forall a : Z, 1 <= a < p -> coprime a p ->
    exists i : Z, 0 <= i < p - 1/\ ((g ^ i) mod p) = a.


(*
  For order, we avoid to give a real definition, since that would 
  require extra contruction on cardinality, generator, groups, orbit, 
  etc. 
  
  We just give the definition for this specific case, where order of g 
  in the multiplicative opeartion of mod p (in (Z/pZ)x) is exactly p - 1.
  *)
Definition order_eq_pminus1 (g p : Z) : Prop :=
  g ^ (p - 1) mod p = 1 /\
  forall k : Z, 0 < k -> k < p - 1 -> g ^ k mod p <> 1.

Theorem primitive_root_order_eq :
  forall g p : Z,
  primitive_root g p ->
  order_eq_pminus1 g p.
Proof.
  (* For simplicity, I give a handwavy proof
    because this requires some construction on group, cardinality, generator, etc.
  
    Let G = (Z/pZ)×, the multiplicative group modulo prime p (without 0).
    By the definition of primitive root, 
      g^i where 0 < i <= p - 1, and for every 1 <= a <= p-1 exists some i such that
      g^i = a.
    Then the group, generated by g (denoted as <g>), must be exactly (Z/pZ)x.
    Now we know |(Z/pZ)×| = p - 1 by counting.
    And ord(g) = |<g>| = |(Z/pZ)×| = p - 1.

    Mathcomp
*)
Admitted.




