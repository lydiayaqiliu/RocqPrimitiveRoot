Lemma primitive_root_injective_aux :
  forall g p m k : Z,
  primitive_root g p ->
  0 <= m < p - 1 ->
  0 <= k < p - 1 ->
  g ^ m mod p = g ^ k mod p ->
  k < m ->
  False.
Proof. intros g p m k H H0 H1 H2 Hlt.
assert (H3 : g ^ m = g ^ (m - k) * g ^ k).
{ apply pow_split. lia. lia. }
rewrite H3 in H2. 
assert (H4 : g^(m - k) mod p = 1 mod p).
{ destruct H as [Hp [Hcp Hpr]].
  apply mod_cancel with (c := g^k).
  + assumption.
  + intros Hcontra. assert (Hcpk : coprime (g ^ k) p).
    { apply coprime_pow.
      + assumption.
      + lia. }
    unfold coprime in Hcpk. apply mod_zero_gcd in Hcontra. 
    - lia.
    - assumption. 
  + replace (1*g ^k ) with (g^k).
    - assumption.
    - lia. }
assert (H5 : ((p - 1) | m - k)%Z).
{ apply order_divides with (g:=g).
  + apply H.
  + apply primitive_root_order_eq. apply H.
  + replace (1 mod p) with 1 in H4. lia. symmetry. apply Z.mod_1_l. unfold primitive_root in H. destruct H as [Hprime _].
    apply prime_ge_2 in Hprime. lia.
  +  replace (1 mod p) with 1 in H4. lia. symmetry. apply Z.mod_1_l. unfold primitive_root in H. destruct H as [Hprime _].
    apply prime_ge_2 in Hprime. lia. }
assert (H6 : m - k = 0).
{ unfold Z.divide in H5.
  destruct H5 as [n Hn].
  assert (Hpge2 : p - 1 >= 1).
  { unfold primitive_root in H. destruct H as [Hprime _].
    apply prime_ge_2 in Hprime. lia. }
  (* m - k >= 0 from Hlt, and m - k < p - 1 from H0, H1 *)
  assert (Hmk_bound : 0 <= m - k < p - 1) by lia.
  rewrite Hn in Hmk_bound.
  apply primitive_root_order_eq in H. unfold order_eq_pminus1 in H.
  destruct H as [Hord Hmin].
  destruct (Z.eq_dec (m - k) 0) as [Heq | Hne].
  - exact Heq.
  - exfalso.
    assert (Hpos : 0 < m - k) by lia.
    apply (Hmin (m - k)).
    + lia.
    + lia.
    + rewrite H4. apply Z.mod_1_l. lia. }
lia.
Qed.

Theorem primitive_root_injective :
  forall g p m k : Z,
  primitive_root g p ->
  0 <= m < p - 1 ->
  0 <= k < p - 1 ->
  g ^ m mod p = g ^ k mod p ->
  m = k.
  Proof. intros. 
    destruct (Z.lt_trichotomy k m) as [Hlt | [Heq | Hgt]].
    - assert (False).
    {
      apply primitive_root_injective_aux with (g := g) (p := p) (m := m)(k := k);
      try (assumption).

    }
    lia.
    - lia.
    - assert (False).
    {
      apply primitive_root_injective_aux with (g := g) (p := p) (m := k)(k := m);
      try (assumption). symmetry. assumption.
    }
    lia.
  Qed.

