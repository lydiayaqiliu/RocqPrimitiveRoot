Theorem Euclidean_division_mod :
  forall r p q s: Z, r > 0 ->
  p > 1 ->
  Z.div_eucl r (p - 1) = (q, s) ->
   s = s mod (p - 1).
Proof.
  intros r p q s Hr Hp Hdiv.

  (* Cast r to positive *)
  assert (Hr_pos : 0 < r) by lia.
  set (r_pos := Z.to_pos r).  (* r as type positive *)

  (* Cast p-1 to N, show it's <> 0%N *)
  assert (Hp1 : 0 < p - 1) by lia.
  set (pm1_N := Z.to_N (p - 1)).  (* p-1 as type N *)
  assert (Hpm1_ne : pm1_N <> 0%N).
  { unfold pm1_N. zify. lia. }

  (* Use mod_pos_bound to get s < p-1 *)
  assert (Hs_bound : s < p - 1).
  {
    assert (Hpos : 0 < p - 1) by lia.
    pose proof (Z.div_eucl_eq r (p - 1)) as Heq.
    pose proof (Z.mod_pos_bound r (p - 1) Hpos) as Hbound.
    unfold Z.modulo in Hbound.
    rewrite Hdiv in Hbound.
    simpl in Hbound.
    lia.
  }

  (* Get s >= 0 from mod_pos_bound *)
  assert (Hs_pos : 0 <= s).
  {
    assert (Hpos : 0 < p - 1) by lia.
    pose proof (Z.mod_pos_bound r (p - 1) Hpos) as Hbound.
    unfold Z.modulo in Hbound.
    rewrite Hdiv in Hbound.
    simpl in Hbound.
    lia.
  }

  (* Apply Z.mod_small *)
  symmetry.
  apply Z.mod_small.
  lia.
Qed.

Lemma Euclidean_division_pos:
  forall r p q s: Z, r > 0 ->
  p > 1 ->
  Z.div_eucl r (p - 1) = (q, s) ->
  0 <= q.
Proof.
  intros r p q s Hr Hp Hdiv.
  assert (Hpos :0 < p - 1) by lia.
  assert (Hrge : 0 <= r) by lia.
  pose proof (Z.div_pos r (p - 1) Hrge Hpos) as Hq.
  unfold Z.div in Hq.
  rewrite Hdiv in Hq.
  simpl in Hq.
  assumption.
Qed.


