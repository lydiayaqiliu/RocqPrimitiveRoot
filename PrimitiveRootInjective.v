Lemma coprime_pow :
  forall g p k : Z,
  coprime g p ->
  0 <= k ->
  coprime (g ^ k) p.
Proof.
  intros.
  unfold coprime in *.
  apply Zgcd_1_rel_prime.
  apply Zgcd_1_rel_prime in H.
  (* convert to nat induction *)
  rewrite <- (Z2Nat.id k H0).
  generalize (Z.to_nat k) as n.
  induction n as [| n IHn].
  - simpl. apply rel_prime_1.
  - rewrite Nat2Z.inj_succ.
  rewrite Z.pow_succ_r by lia.
  apply rel_prime_sym.
  eapply rel_prime_mult.
  + apply rel_prime_sym. assumption.
  + apply rel_prime_sym. apply IHn.
Qed.


