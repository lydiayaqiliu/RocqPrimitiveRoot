Lemma mod_zero_gcd :
  forall a p : Z,
  prime p ->
  a mod p = 0 ->
  Z.gcd a p = p.

Proof. intros a p Hprime Hmod.
  apply Z.mod_divide in Hmod; [| apply prime_ge_2 in Hprime; lia].
  (* gcd(a, p) divides p, and p is prime, so gcd = 1 or gcd = p *)
  (* Since p | a, we have p | gcd(a, p), so gcd = p *)
  apply Z.gcd_unique.
  - apply prime_ge_2 in Hprime; lia.
  - assumption.
  - apply Z.divide_refl.
  - intros.
  apply H0.
Qed.

